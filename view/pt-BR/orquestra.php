            <p>A Orquestra Sinf&ocirc;nica do Rio Grande do Norte foi criada pelo do Decreto 
                nº 6874 de mar&ccedil;o de 1976, assinado pelo ent&atilde;o Governador Tarc&iacute;sio Maia, atrav&eacute;s 
                de iniciativa do, &agrave; &eacute;poca, Secret&aacute;rio de Educa&ccedil;&atilde;o e Cultura, Professor 
                Jo&atilde;o Faustino, passando a pertencer aos quadros administrativos da mesma 
                Secretaria.</p>
            <p>O primeiro concerto foi realizado em 11 de mar&ccedil;o de 1977, e teve como 
                maestro o pernambucano M&aacute;rio C&acirc;ncio Justo dos Santos, que organizou e regeu a 
                orquestra por aproximadamente 10 anos. A orquestra come&ccedil;ou com um quadro 
                de 23 m&uacute;sicos efetivos, dos quais apenas cinco residiam na cidade do 
                Natal, e executava um repert&oacute;rio barroco e camer&iacute;stico.</p>
            <p>O primeiro concurso p&uacute;blico para ingresso na Orquestra Sinf&ocirc;nica aconteceu 
                em 1988, na gest&atilde;o do ent&atilde;o Governador Geraldo Melo. Assim, a orquestra foi 
                ampliada e passou a contar com 60 m&uacute;sicos. Nessa mesma &eacute;poca o violinista 
                e professor Osvaldo D&apos;Amore passou a ser o novo regente da orquestra, 
                cargo no qual permaneceu por quase 20 anos.</p>
            <p>A Orquestra desde ent&atilde;o passou a realizar um prof&iacute;cuo trabalho apresentando 
                concertos oficiais no Teatro Alberto Maranh&atilde;o, concertos populares externos, 
                concertos educativos para toda a rede de ensino, bem como, concertos 
                especiais no interior do Estado. A OSRN tem tr&ecirc;s CDs gravados, dentre 
                os quais um com a participa&ccedil;&atilde;o dos artistas Sivuca, Canhoto da Para&iacute;ba, 
                Moraes Moreira e Henrique Cazes.</p>
            <p>Desde a sua funda&ccedil;&atilde;o, a Orquestra Sinf&ocirc;nica contou com a colabora&ccedil;&atilde;o de 
                v&aacute;rios diretores administrativos. A primeira foi Zuleika Romano, que esteve no 
                cargo durante todo o per&iacute;odo do ent&atilde;o maestro M&aacute;rio C&acirc;ncio. Posteriormente 
                assumiram o cargo: Selma S&aacute;, Diana Fontes, Olga Aranha, Deijair Borges, 
                Candinha Bezerra, Gina Cavalcanti, Fidja Siqueira, Paulo Henrique de 
                Lima (primeiro m&uacute;sico titular da OSRN a ocupar o cargo), Francisco Jos&eacute; 
                Alves, Maria das Dores, e atualmente Luiz Antonio Paiva (primeiro m&uacute;sico 
                titular da OSRN escolhido diretamente por seus pares).</p>
            <p>Em 2007 assumiu como regente da Orquestra Sinf&ocirc;nica o Maestro Andr&eacute; Muniz; 
                em 2011 assumiu por um curto per&iacute;odo o Regente Pe. Pedro Ferreira; e em setembro 
                de 2012 assumiu o atual Maestro Linus Lerner. Na atual temporada de 2018, 
                a OSRN executa um projeto com regentes e solistas convidados, nacionais 
                e internacionais, o que proporciona um crescimento musical imensur&aacute;vel 
                para os m&uacute;sicos integrantes da OSRN, j&aacute; que cada profissional convidado 
                compartilha com a Orquestra sua concepção musical, sua experiência 
                art&iacute;stica e sua interpreta&ccedil;&atilde;o para cada obra executada.</p>
            <p>Atualmente a Orquestra conta com 60 m&uacute;sicos que ensaiam diariamente na Cidade da Crian&ccedil;a, sede atual da OSRN, e continua com todo afinco a realizar 
                um trabalho tanto educativo com a forma&ccedil;&atilde;o de novas plateias, quanto 
                de difus&atilde;o da m&uacute;sica de qualidade, seja ela erudita ou popular. Um 
                trabalho de suma import&acirc;ncia para o desenvolvimento cultural do Estado 
                do Rio Grande do Norte.</p>
