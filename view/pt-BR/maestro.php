            <p>O Maestro Linus Lerner é elogiado por  sua fluência musical e 
                carisma tanto no podium sinfônico e coral, quanto no fosso da ópera. 
                Linus regeu vários grupos nos Estados Unidos, Brasil, Bulgária, China, República Checa, 
                México, Espanha e Turquia.</p>
            <p>Atualmente mantém o cargo de diretor artístico e regente da <i>Southern Arizona Symphony Orchestra</i> (SASO) 
                em Tucson-AZ, da Orquestra Sinfonica do Rio Grande do Norte (OSRN) e do Festival de Opera de Oaxaca no Mexico.</p>
            <p>Com a SASO, realiza temporada regular de 14 concertos anuais, além de outros eventos. 
                Com a mesma realizou também duas tournees por várias cidades Chinesas  e gravação de um CD de compositores 
                da cidade de Tucson.</p> 
            <p>Como regente e diretor artístico da OSRN, tem realizado diversos concertos e eventos na cidade 
                de Natal.</p>
            <p>Para assumir o cargo de diretor artístico da OSRN, o maestro Linus Lerner recentemente deixou seu 
                cargo como regente e diretor artístico dos coros da BCPA com os quais realizou inúmeras performances nos 
                teatros mais prestigiados de Houston, além de gravações de Cd's e performances com a 
                <i>Houston Pops Orchestra</i> e a <i>Texas Medical Center Orquestra</i>.  Com esta última, acaba de 
                fazer seu <i>debut</i> no <i>Carnegie Hall</i> em maio de 2013.</p>
            <!--</article>-->
            <!--<article id="texto2">-->
            <p>Linus Lerner tem atuado também como regente associado para a <i>Opera de Ozarks</i>, A Orquestra 
                Jovem de Tallahassee e a Orquestra Philarmonica da Universidade do Arizona.</p>
            <p>Como maestro convidado, tem regido a <i>Orchestra Nacional de Valles</i> na Espanha;  a Orquestra 
                Sinfônica de Porto Alegre; a <i>International Opera Workshop</i> da Bulgária e República Tcheca; 
                os Solistas <i>Ensamble del Instituto Nacional de Bellas Artes</i> do Mexico;  a Companhia de Ópera 
                do <i>Brooklyn, New York</i>; a Ópera da Universidade de Colorado em Boulder; a Ópera <i>in the 
                    Heights</i> em Houston, a Orquestra Sinfônica de <i>Hunan</i> na China,  a Orquestra <i>Tsintao</i>
                na China, a Orquestra de <i>Fuzhou</i> na China, a Orquestra Primavera no Mexico, a 
                <i>Wick Chamber Orchestra and Singers</i> no Arizona e foi regente assistente da Ópera do Arizona. 
                No ano de 2011 foi diretor artístico do Festival de Ópera de <i>Beijing</i> que culminou com a 
                produção da “Flauta Mágica” com a orquestra Nacional da Rádio e Televisão Chinesa e cantores da 
                Opera Nacinal Chinesa.  Em 2012 realizou a segunda edição do festival em <i>Beijing</i>  
                além da primeira  edição como diretor artístico do Festival de Oaxaca no México.</p>
            <p>Linus Lerner completou seu doutorado em regência de orquestra pela Universidade do Arizona (UA) onde 
                foi regente assistente de Ópera e da orquestra sinfônica da mesma, regendo várias performances em <i>Tucson</i>, 
                bem como na <i>tournee</i> pelo México em 2002.</p>
            <p>Linus possui também dois mestrados em música: um em regência orquestral pela Universidade Estadual 
                da Flórida onde foi regente assistente das orquestras, apresentando-se em inúmeras performances incluindo a 
                <i>Premiere</i> Americana da Ópera <i>Procedura Penale</i> de Luciano Chally; o segundo em 
                Performance Vocal (ópera) pelo Conservatório de Música da Universidade de Cincinnati. Também possui 
                um bacharelado em regência coral pela Universidade Federal do Rio Grande do Sul.</p>
            <p>Além de sua experiência como regente orquestral e de ópera,  Dr. Linus Lerner possui uma vasta 
                experiência como regente de coros, Dr. Linus Lerner trabalhou com mais de 50 grupos de vários tamanhos e 
                combinações de vozes, além de participar de inúmeras tournees, festivais e competições. 
                Linus tem dado cursos e proporcionado aperfeiçoamento a vários coros profissionais,  de escolas, comunidades e igrejas tanto no Brasil como no México e Estados Unidos.</p>
            <p>Como cantor, Linus tem participado em varias óperas e concertos nos Estados Unidos, Europa, Brasil e 
                México e tem recebido prêmios em competicões como o Concurso <i>Neyde Thomas</i>, Concurso para Solistas da 
                OSPA e <i>Miami University Concerto Competition</i>.</p>
            <p>Linus Lerner atua ainda como professor de canto e <i>'voice coach'</i> e viaja regularmente para 
                ensinar cantores e alunos de regência nos Estados Unidos, Mexico, Brasil e China.</p>
